/* Microsecond delay using DWT cycle counter */
void delay_us(uint32_t us)
{
    uint32_t start = DWT->CYCCNT;
    uint32_t ticks = us * (SystemCoreClock / 1000000);
    while ((DWT->CYCCNT - start) < ticks);
}

/* Ultrasonic measurement using DWT */
float read_distance(void)
{
    /* 10us TRIG pulse */
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_SET);
    delay_us(10);
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, GPIO_PIN_RESET);

    /* Wait for ECHO HIGH */
    uint32_t timeout = 30000;
    while (!HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_9))
        if (--timeout == 0) return 999;

    uint32_t start_tick = DWT->CYCCNT;

    /* Wait for ECHO LOW */
    timeout = 30000;
    while (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_9))
        if (--timeout == 0) break;

    uint32_t end_tick = DWT->CYCCNT;

    uint32_t pulse_us = (end_tick - start_tick) / (SystemCoreClock / 1000000);

    float distance_cm = pulse_us / 58.0;
    return distance_cm;
}
